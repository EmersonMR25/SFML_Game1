/**
 * @file definitions.h
 * @brief Contains global constant definitions for window dimensions
 *
 * This header file defines global constants used throughout the application,
 * particularly for setting up the window size and boundaries for the particle simulation.
 */

#pragma once
#ifndef DEFINITIONS_H
#define DEFINITIONS_H

/**
 * @brief Defines the width of the application window
 *
 * The constant represents the width of the window in pixels.
 * Used for setting up the window and calculating boundary collisions.
 */
#define _WIDTH 1366.f

/**
 * @brief Defines the height of the application window
 *
 * The constant represents the height of the window in pixels.
 * Used for setting up the window and calculating boundary collisions.
 */
#define _HEIGHT 768.f

#endif // DEFINITIONS_H